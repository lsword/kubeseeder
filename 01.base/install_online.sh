#!/bin/bash

#==================================================================================================================
# Load config
#------------------------------------------------------------------------------------------------------------------
OS_RELEASE=$(yq '.os.release' ../config.yaml)

#==================================================================================================================
# Exec install script
#------------------------------------------------------------------------------------------------------------------
if [ $OS_RELEASE == "centos" ]; then
  ./install_online_centos.sh
elif [ $OS_RELEASE == "ubuntu" ]; then
  ./install_online_ubuntu.sh
else
  echo "Does not support for $OS_RELEASE"
  exit 1
fi
